package beans;

public class Informacoes {

	private String arroz, feijao, farinha, acucar, oleo, sal, milho, ervilha;
	int quantidade;
	
	
	public int getQuantidade() {
		return quantidade;
	}

	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}

	public String getArroz() {
		return arroz;
	}

	public void setArroz(String arroz) {
		this.arroz = arroz;
	}

	public String getFeijao() {
		return feijao;
	}

	public void setFeijao(String feijao) {
		this.feijao = feijao;
	}

	public String getFarinha() {
		return farinha;
	}

	public void setFarinha(String farinha) {
		this.farinha = farinha;
	}

	public String getAcucar() {
		return acucar;
	}

	public void setAcucar(String acucar) {
		this.acucar = acucar;
	}

	public String getOleo() {
		return oleo;
	}

	public void setOleo(String oleo) {
		this.oleo = oleo;
	}

	public String getSal() {
		return sal;
	}

	public void setSal(String sal) {
		this.sal = sal;
	}

	public String getMilho() {
		return milho;
	}

	public void setMilho(String milho) {
		this.milho = milho;
	}

	public String getErvilha() {
		return ervilha;
	}

	public void setErvilha(String ervilha) {
		this.ervilha = ervilha;
	}
	
}
