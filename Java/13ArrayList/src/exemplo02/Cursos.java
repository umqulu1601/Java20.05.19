package exemplo02;

public class Cursos {

	//Atributo
		private String nomeCurso, areaCurso;
		private double valorCurso;
		
		//Set & Get
		public String getNomeCurso() {
			return nomeCurso;
		}
		public void setNomeCurso(String nomeCurso) {
			this.nomeCurso = nomeCurso;
		}
		public String getAreaCurso() {
			return areaCurso;
		}
		public void setAreaCurso(String areaCurso) {
			this.areaCurso = areaCurso;
		}
		public double getValorCursos() {
			return valorCurso;
		}
		public void setValorCursos(double valorCursos) {
			this.valorCurso = valorCursos;
		}
		
	
}
